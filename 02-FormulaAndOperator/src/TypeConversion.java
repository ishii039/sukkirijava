
public class TypeConversion {

	public static void main(String[] args) {
		float f = 3;
		double d = f;
		System.out.println(f);
		System.out.println(d);

		int age = (int)3.2;
		System.out.println(age);

		double dd = 8.5 / 2;
		long l = 5 + 2L;
		System.out.println(dd);
		System.out.println(l);

		String msg = "私の年齢は" + 23;
		System.out.println(msg);
		
		


	}

}
